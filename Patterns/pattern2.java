package Patterns;
import java.util.Scanner;
public class pattern2 {
    public static void main(String[] args) {
        int n ; 
        Scanner s = new Scanner(System.in);
        n = s.nextInt();
        for (int i = n ; i >=1; i--)
        {
            for (int j = n ; j >= 1 ; j--)
            {
                System.out.print(n);
            }
            {
                System.out.println();
            }
        }
    }
}